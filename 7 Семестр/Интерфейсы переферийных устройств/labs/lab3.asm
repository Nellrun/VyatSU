drawImage proc near
  mov bp, sp
  pusha

  ;получаем адрес изображения
  mov bx, [bp+2]
  mov cx, [bp+4]
  mov dx, [bp+6]
  mov ah, 0ch
  mov di, 0
  mov si, 0
  ;основной цикл рисования
  inc bx
  mov al, [bx]
  drawLoop:
    inc cx
    inc si
    cmp si, 172
    ; если дошли до конца строки изображения, то переход на новую
    jne notEndLine
    mov si, 0
    sub cx, 172
    inc dx
    cmp dx, 200
    jg exit
    notEndLine:
    cmp di, 4
    jnz notZero
    mov di, 0
    inc bx
    mov al, [bx]
    notZero:
    int 10h
    cmp al, 0FBh
    je exit
    shr al, 2
    inc di
    jmp drawLoop
  exit:

  popa
  ret 6
drawImage endp

